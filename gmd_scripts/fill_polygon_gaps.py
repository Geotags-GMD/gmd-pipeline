    def _find_target_feature(
        self, layer, extent_rect, label_field, feature_value, feedback
    ):
        """
        Locate the target feature by field value or FID.
        
        If label_field is provided, searches features within the extent
        for a matching field value. Otherwise, looks up by FID.
        
        Returns:
            QgsFeature: The matched feature.
        Raises:
            QgsProcessingException: If no matching feature is found.
        """
        if label_field:
            # ── Search by field value ────────────────────────────────────
            field_idx = layer.fields().indexOf(label_field)
            if field_idx < 0:
                raise QgsProcessingException(
                    f"Field '{label_field}' not found in layer '{layer.name()}'.\n"
                    f"Available fields: {', '.join(f.name() for f in layer.fields())}"
                )

            request = QgsFeatureRequest()
            if extent_rect and not extent_rect.isEmpty():
                request.setFilterRect(extent_rect)
            candidates = []

            for feat in layer.getFeatures(request):
                val = str(feat[label_field]).strip()
                if val == feature_value:
                    return feat
                candidates.append(val)

            # Not found — provide helpful message
            if candidates:
                unique = sorted(set(candidates))[:20]
                hint = ', '.join(f"'{v}'" for v in unique)
                raise QgsProcessingException(
                    f"No feature with {label_field}='{feature_value}' "
                    f"found in the extent.\n"
                    f"Available values (up to 20): {hint}"
                )
            else:
                raise QgsProcessingException(
                    f"No features found within the extent to search."
                )
        else:
            # ── Search by FID ────────────────────────────────────────────
            try:
                fid = int(feature_value)
            except ValueError:
                raise QgsProcessingException(
                    f"'{feature_value}' is not a valid FID number.\n"
                    f"Either enter a numeric FID, or select a label field "
                    f"from the dropdown above."
                )

            target = layer.getFeature(fid)
            if not target.isValid() or target.geometry().isEmpty():
                # List available FIDs in extent for guidance
                request = QgsFeatureRequest()
                if extent_rect and not extent_rect.isEmpty():
                    request.setFilterRect(extent_rect)
                available = [
                    str(f.id()) for f in layer.getFeatures(request)
                ][:20]
                raise QgsProcessingException(
                    f"No valid feature with FID={fid}.\n"
                    f"FIDs in extent (up to 20): {', '.join(available)}"
                )

            return target
