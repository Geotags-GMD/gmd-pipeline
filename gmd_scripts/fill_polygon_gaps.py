    #  POST-PROCESSING: Auto-style the preview layer
    # =========================================================================

    def postProcessAlgorithm(self, context, feedback):
        """Apply categorized style to the single preview layer with outlines for original target."""
        try:
            if hasattr(self, 'preview_layer_id') and self.preview_layer_id:
                lyr_preview = QgsProcessingUtils.mapLayerFromString(self.preview_layer_id, context)
                if lyr_preview:
                    # 1. Original (outline blue, transparent fill)
                    sym_orig = QgsSymbol.defaultSymbol(QgsWkbTypes.PolygonGeometry)
                    sym_orig.setColor(QColor(0, 0, 0, 0))  # transparent fill
                    sym_orig.symbolLayer(0).setFillColor(QColor(0, 0, 0, 0))
                    sym_orig.symbolLayer(0).setStrokeColor(QColor(52, 152, 219))  # Blue
                    sym_orig.symbolLayer(0).setStrokeWidth(1.5)

                    # 2. Gap Fill (semi-transparent red fill)
                    sym_gap = QgsSymbol.defaultSymbol(QgsWkbTypes.PolygonGeometry)
                    sym_gap.setColor(QColor(231, 76, 60, 100))  # Red (semi-transparent)
                    sym_gap.symbolLayer(0).setFillColor(QColor(231, 76, 60, 100))
                    sym_gap.symbolLayer(0).setStrokeColor(QColor(192, 57, 43))
                    sym_gap.symbolLayer(0).setStrokeWidth(0.5)

                    # 3. Result (semi-transparent green fill)
                    sym_res = QgsSymbol.defaultSymbol(QgsWkbTypes.PolygonGeometry)
                    sym_res.setColor(QColor(46, 204, 113, 100))  # Green (semi-transparent)
                    sym_res.symbolLayer(0).setFillColor(QColor(46, 204, 113, 100))
                    sym_res.symbolLayer(0).setStrokeColor(QColor(39, 174, 96))
                    sym_res.symbolLayer(0).setStrokeWidth(0.5)

                    categories = [
                        QgsRendererCategory('original', sym_orig, 'Original Target'),
                        QgsRendererCategory('gap_fill', sym_gap, 'Gap Filled'),
                        QgsRendererCategory('result', sym_res, 'Final Result')
                    ]

                    renderer = QgsCategorizedSymbolRenderer('role', categories)
                    lyr_preview.setRenderer(renderer)
                    lyr_preview.triggerRepaint()
        except Exception as e:
            feedback.pushInfo(f"Note: Could not auto-style preview layer: {e}")

        return {}

