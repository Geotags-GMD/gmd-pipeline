class FeatureValueWidgetWrapper(WidgetWrapper):
    """
    Custom widget wrapper for the polygon identifier value parameter.
    Dynamically populates a QComboBox with unique values from the chosen
    field of the selected input layer. If an extent is defined (either manual
    or via extent layer), only values/FIDs from features intersecting the
    combined extent are listed.
    """
    def createWidget(self):
        self.combo = QComboBox()
        self.combo.setEditable(True)  # Allow typing custom/manually entered values
        self.combo.currentTextChanged.connect(self.on_combo_changed)
        
        self.layer_wrapper = None
        self.field_wrapper = None
        self.extent_mode_wrapper = None
        self.extent_wrapper = None
        self.extent_layer_wrapper = None
        return self.combo

    def postInitialize(self, wrappers):
        # Locate the parent wrappers to listen to their updates
        for w in wrappers:
            name = w.parameterDefinition().name()
            if name == 'INPUT_LAYER':
                self.layer_wrapper = w
                w.widgetValueHasChanged.connect(self.on_parent_changed)
            elif name == 'LABEL_FIELD':
                self.field_wrapper = w
                w.widgetValueHasChanged.connect(self.on_parent_changed)
            elif name == 'EXTENT_MODE':
                self.extent_mode_wrapper = w
                w.widgetValueHasChanged.connect(self.on_parent_changed)
            elif name == 'EXTENT':
                self.extent_wrapper = w
                w.widgetValueHasChanged.connect(self.on_parent_changed)
            elif name == 'EXTENT_LAYER':
                self.extent_layer_wrapper = w
                w.widgetValueHasChanged.connect(self.on_parent_changed)
        
        # Initial population of options
        self.on_parent_changed()

    def on_combo_changed(self, text):
        self.widgetValueHasChanged.emit(self)

    def on_parent_changed(self):
        if not self.layer_wrapper:
            return
        
        # Use widgetValue() instead of value() to avoid AttributeError
        layer_val = self.layer_wrapper.widgetValue()
        layer = None
        
        if isinstance(layer_val, QgsMapLayer):
            layer = layer_val
        elif isinstance(layer_val, str) and layer_val:
            layer = QgsProject.instance().mapLayer(layer_val)
            if not layer:
                layers = QgsProject.instance().mapLayersByName(layer_val)
                if layers:
                    layer = layers[0]
        
        if not layer:
            self.combo.clear()
            return
            
        field_name = self.field_wrapper.widgetValue() if self.field_wrapper else ''
        
        # Determine the selected extent mode
        extent_mode = 0
        if self.extent_mode_wrapper:
            try:
                val = self.extent_mode_wrapper.widgetValue()
                if isinstance(val, int):
                    extent_mode = val
                else:
                    val_str = str(val).lower()
                    if 'layer' in val_str:
                        extent_mode = 1
                    else:
                        extent_mode = 0
            except Exception:
                pass

        extent_rect = None
        
        if extent_mode == 0:
            # Mode 0: Manual Extent
            if self.extent_wrapper:
                extent_val = self.extent_wrapper.widgetValue()
                if isinstance(extent_val, QgsRectangle):
                    extent_rect = extent_val
                elif isinstance(extent_val, str) and extent_val:
                    try:
                        rect_res = QgsProcessingUtils.stringToRectangle(extent_val)
                        if rect_res and len(rect_res) > 0:
                            extent_rect = rect_res[0]
                    except Exception:
                        pass
        else:
            # Mode 1: Extent Layer
            if self.extent_layer_wrapper:
                layer_val = self.extent_layer_wrapper.widgetValue()
                ext_layer = None
                if isinstance(layer_val, QgsMapLayer):
                    ext_layer = layer_val
                elif isinstance(layer_val, str) and layer_val:
                    ext_layer = QgsProject.instance().mapLayer(layer_val)
                    if not ext_layer:
                        layers = QgsProject.instance().mapLayersByName(layer_val)
                        if layers:
                            ext_layer = layers[0]
                if ext_layer:
                    combined = QgsRectangle()
                    for f in ext_layer.getFeatures():
                        geom = f.geometry()
                        if geom and not geom.isEmpty():
                            combined.combineExtentWith(geom.boundingBox())
                    if not combined.isEmpty():
                        extent_rect = combined

        request = QgsFeatureRequest()
        if extent_rect and not extent_rect.isEmpty():
            request.setFilterRect(extent_rect)

        # Block signals to avoid infinite recursion while repopulating
        self.combo.blockSignals(True)
        current_text = self.combo.currentText()
        self.combo.clear()
        
        items = []
        if field_name:
            field_idx = layer.fields().indexOf(field_name)
            if field_idx >= 0:
                if extent_rect and not extent_rect.isEmpty():
                    # Filter unique values only to features intersecting the extent
                    unique_vals = set()
                    # Optimize: only fetch the needed field
                    request.setSubsetOfAttributes([field_name], layer.fields())
                    for feat in layer.getFeatures(request):
                        val = feat[field_name]
                        if val is not None and str(val).strip():
                            unique_vals.add(str(val).strip())
                    items = sorted(list(unique_vals))
                else:
                    # Fallback to all unique values in the entire layer
                    unique_vals = layer.uniqueValues(field_idx)
                    items = sorted([
                        str(v).strip() for v in unique_vals 
                        if v is not None and str(v).strip()
                    ])
        else:
            # Fallback to listing Feature IDs (FIDs) intersecting the extent
            # Optimize: do not fetch any attributes when getting FIDs
            request.setNoAttributes()
            items = [str(f.id()) for f in layer.getFeatures(request)]
            if len(items) > 1000:
                items = items[:1000]
                
        self.combo.addItems(items)
        
        # Restore previous value if it's still available in the new items list
        idx = self.combo.findText(current_text)
        if idx >= 0:
            self.combo.setCurrentIndex(idx)
        elif items:
            self.combo.setCurrentIndex(0)
            
        self.combo.blockSignals(False)
        self.widgetValueHasChanged.emit(self)

    def value(self):
        return self.combo.currentText()

    def setValue(self, value):
        self.combo.blockSignals(True)
        idx = self.combo.findText(str(value))
        if idx >= 0:
            self.combo.setCurrentIndex(idx)
        else:
            self.combo.setEditText(str(value))
        self.combo.blockSignals(False)

    # Overrides for newer QGIS 3 APIs
    def widgetValue(self):
        return self.combo.currentText()

    def setWidgetValue(self, value, context):
        self.combo.blockSignals(True)
        idx = self.combo.findText(str(value))
        if idx >= 0:
            self.combo.setCurrentIndex(idx)
        else:
            self.combo.setEditText(str(value))
        self.combo.blockSignals(False)



